  <h2>No Personal Data Collection</h2>

  <p>
    Photon is designed with privacy in mind. The official instance at
    https://phtn.app operates entirely client-side, meaning all code runs on
    your device via your browser. Photon does not store, log, or transmit any
    personal data from your device to any server, including ours.
  </p>
  <h2>How Photon Works</h2>
  <p>
    When you use Photon, your device connects directly to your chosen Lemmy
    instance through API requests. Photon serves only as a client to display
    content and manage interactions with Lemmy. These API requests are initiated
    and handled within your browser, so Photon does not intercept or log this
    data.
  </p>
  <h2>Data Storage</h2>
  <p>
    Photon does not persistently store any information on external servers. Any
    settings or data you configure are stored locally on your device (in your
    browser's local storage). This data is not accessible to Photon or any third
    party and remains entirely on your device.
  </p>
  <h2>Third-Party Links and Content</h2>
  <p>
    Since Photon accesses Lemmy instances, the privacy practices of each Lemmy
    instance may vary. We recommend reviewing the privacy policy of your chosen
    Lemmy instance to understand their data practices, as Photon has no control
    over these.
  </p>
  <h2>Cloudflare Pages Hosting</h2>
  <p>
    phtn.app is hosted on Cloudflare Pages, which serves the app's static assets
    directly to your device. phtn.app does not utilize server-side rendering
    (SSR), so all interactions are confined to your browser.
  </p>
</section>
